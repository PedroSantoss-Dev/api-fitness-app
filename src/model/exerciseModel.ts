import mongoose from "mongoose";

const ExeciseShema = new mongoose.Schema({
  name: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  category: {
    type: String,
    required: true,
  },
});

const Exercise = mongoose.model("Exercise", ExeciseShema);
export default Exercise;
